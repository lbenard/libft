#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	int	diff;

	while (n--)
		if ((diff = *((t_u8*)s1++) - *((t_u8*)s2++)))
			return (diff);
	return (0);
}
